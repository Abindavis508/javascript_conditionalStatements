let vehicletype = "b";
switch (vehicletype) {
    case "c":
        console.log("you have a car");
        break;
    case "b":
        console.log("you have a bike");
        break;
    case "t":
        console.log("you have a truck");
        break;
    case "a":
        console.log("you have a auto");
        break;
        default:
            console.log("Unknown vehicle");
            break;

}